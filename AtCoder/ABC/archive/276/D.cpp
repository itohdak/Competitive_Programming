map<ll, int> prime_factorization(ll N) {
  map<ll, int> res;
  if(N == 1 || N == 0) return res;
  ll n = N;
  ll i = 2;
  while(n >= i * i) {
    while(n % i == 0) {
      if(res.count(i)) res[i]++;
      else res[i] = 1;
      n /= i;
    }
    if(i == 2) i++;
    else i += 2;
  }
  if(n != 1) res[n]++;
  return res;
}

