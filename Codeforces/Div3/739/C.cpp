void solve() {
  ll k; cin >> k;
  k--;
  ll ok = 0, ng = 100000;
  while(abs(ok-ng)>1) {
    ll mid = (ok+ng)/2;
    (k >= mid*mid ? ok : ng) = mid;
  }
  ll rem = k - ok*ok;
  // cout << rem << ' ' << ok << endk;
  if(rem <= ok) {
    cout << rem+1 << ' ' << ok+1 << endk;
  } else {
    rem -= ok;
    cout << ok+1 << ' ' << ok+1-rem << endk;
  }
}
int main() {
  cin.tie(0);
  ios::sync_with_stdio(false);
  int T;
  cin >> T;
  while(T--) solve();
  return 0;
}
